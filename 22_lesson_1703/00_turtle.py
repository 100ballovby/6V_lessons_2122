from turtle import *


t = Turtle()  # главный рисователь
t.shape('turtle')  # задаю форму черепашки

t.fd(100)  # идти вперед на 100 шагов
t.rt(45)  # повернуть вправо на 45 градусов
t.bk(50)  # идти назад 50 шагов
t.lt(124)  # повернуться влево на 124 градуса
t.fd(100)

done()  # чтобы окно не закрывалось сразу

